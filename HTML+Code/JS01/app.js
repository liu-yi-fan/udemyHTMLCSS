let input = prompt("Enter a month.");

switch(input){
    case "January":
        alert("一月");
        break;
    case "February":
        alert("二月");
        break;
    case "March":
        alert("三月");
        break;
    case "April":
        alert("四月");
        break;
    case "May":
        alert("五月");
        break;
    case "June":
        alert("六月");
        break;
    case "July":
        alert("七月");
        break;
    case "August":
        alert("八月");
        break;
    case "Septemper":
        alert("九月");
        break;
    case "October":
        alert("十月");
        break;
    case "November":
        alert("十一月");
        break;
    case "December":
        alert("十二月");
        break;
    default:
        alert("無法判定");
        break;
}

